import 'package:meta/meta.dart';

class Artist{
  String name;
  String desc;
  String imgDp;
  String email;
  String fb;
  String tw;
  String ig;
  String id;

  Artist({
    @required this.name,
    @required this.desc,
    @required this.imgDp,
    @required this.email,
    @required this.fb,
    @required this.tw,
    @required this.ig,
    @required this.id
  });
}