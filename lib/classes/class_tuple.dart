class Tuple2<T,U>{
  final T item1;
  final U item2;

  Tuple2(this.item1, this.item2);
}

class Tuple3<T,U, V>{
  final T item1;
  final U item2;
  final V item3;

  Tuple3(this.item1, this.item2, this.item3);
}

class Tuple4<T,U,V,W>{
  final T item1;
  final U item2;
  final V item3;
  final W item4;

  Tuple4(this.item1, this.item2, this.item3, this.item4);
}